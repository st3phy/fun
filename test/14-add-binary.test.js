const { addBinary } = require("../src/14-add-binary.js");

describe("Add Binary", () => {
    const tests = [
        { args: ["11", "1"], res: "100" },
        { args: ["1010", "1011"], res: "10101" },
        { args: ["0", "0"], res: "0" },
        { args: ["1", "0"], res: "1" },
        {
            args: [
                "10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101",
                "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011"
            ],
            res: "110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000"
        },
        {
            args: ["1111100001101", "11011110011"],
            res: "10011000000000"
        }
    ];

    for (const { args, res } of tests) {
        test(`${JSON.stringify(args)}: ${res}`, () => {
            expect(addBinary(...args)).toStrictEqual(res);
        });
    }
});
