const { generateParenthesis } = require("../src/22-generate-parentheses.js");

describe("Generate Parentheses", () => {
    const tests = [
        { n: 1, res: ["()"] },
        { n: 3, res: ["((()))", "(()())", "(())()", "()(())", "()()()"] },
        {
            n: 4,
            res: [
                "()()()()",
                "(()()())",
                "()(()())",
                "(()())()",
                "((()()))",
                "()()(())",
                "()(())()",
                "(()(()))",
                "(())()()",
                "((())())",
                "()((()))",
                "((()))()",
                "(((())))",
                "(())(())"
            ]
        },
        {
            n: 5,
            res: [
                "((((()))))",
                "(((()())))",
                "(((())()))",
                "(((()))())",
                "(((())))()",
                "((()(())))",
                "((()()()))",
                "((()())())",
                "((()()))()",
                "((())(()))",
                "((())()())",
                "((())())()",
                "((()))(())",
                "((()))()()",
                "(()((())))",
                "(()(()()))",
                "(()(())())",
                "(()(()))()",
                "(()()(()))",
                "(()()()())",
                "(()()())()",
                "(()())(())",
                "(()())()()",
                "(())((()))",
                "(())(()())",
                "(())(())()",
                "(())()(())",
                "(())()()()",
                "()(((())))",
                "()((()()))",
                "()((())())",
                "()((()))()",
                "()(()(()))",
                "()(()()())",
                "()(()())()",
                "()(())(())",
                "()(())()()",
                "()()((()))",
                "()()(()())",
                "()()(())()",
                "()()()(())",
                "()()()()()"
            ]
        }
    ];

    const sorter = (a, b) => {
        a = a.replace(/\(/g, "1").replace(/\)/g, "2");
        b = b.replace(/\(/g, "1").replace(/\)/g, "2");

        return a - b;
    };

    for (const { n, res } of tests) {
        test(`${n}: ${res}`, () => {
            const strRes = res.sort(sorter).join(", ");
            const strResult = generateParenthesis(n).sort(sorter).join(", ");
            expect(strRes).toStrictEqual(strResult);
        });
    }
});
